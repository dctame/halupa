     * Display the specified resource.
     *
     * @param  \App\Location  $location
     * @return \Illuminate\Http\Response
     */
    public function show(Location $location, Request $request)
    {

        $id = ($request->get('id'))?$request->get('id'):1;

        $location = $location->find($id);
        $user = auth()->user();

        $content['name'] = $location->getAttribute('name');
        $content['pathes'] = $location->getPathesAttribute();
        $content['items'] = $location->locationItems($user->id);
        $content['inventory'] = $user->getInventory();
        $content['picture'] = $location->img;
        $content['description'] = html_entity_decode($location->getAttribute('description'));


        return response($content, 200);


    }

